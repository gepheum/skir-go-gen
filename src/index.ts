const Config = z.strictObject({
  goModuleName: z
    .string()
    .regex(/^[a-zA-Z0-9][a-zA-Z0-9._~-]*(\/[a-zA-Z0-9][a-zA-Z0-9._~-]*)*$/),
});

type Config = z.infer<typeof Config>;

class GoCodeGenerator implements CodeGenerator<Config> {
  readonly id = "skir-go-gen";
  readonly configType = Config;

  generateCode(input: CodeGenerator.Input<Config>): CodeGenerator.Output {
    const { recordMap, config } = input;
    const outputFiles: CodeGenerator.OutputFile[] = [];
    for (const module of input.modules) {
      const packagePath = modulePathToPackagePath(module.path);
      const packageName = modulePathToPackageName(module.path);
      outputFiles.push({
        path: packagePath.concat(`/${packageName}.go`),
        code: new GoSourceFileGenerator(module, recordMap, config).generate(),
      });
    }
    return { files: outputFiles };
  }
}

// Generates the code for one Go file.
class GoSourceFileGenerator {
  constructor(
    private readonly inModule: Module,
    recordMap: ReadonlyMap<RecordKey, RecordLocation>,
    private readonly config: Config,
  ) {
    this.typeSpeller = new TypeSpeller(recordMap, inModule.path);
  }

  generate(): string {
    const packageName = modulePathToPackageName(this.inModule.path);

    // http://patorjk.com/software/taag/#f=Doom&t=Do%20not%20edit
    this.push(
      `//  ______                        _               _  _  _
      //  |  _  \\                      | |             | |(_)| |
      //  | | | |  ___    _ __    ___  | |_    ___   __| | _ | |_
      //  | | | | / _ \\  | '_ \\  / _ \\ | __|  / _ \\ / _\` || || __|
      //  | |/ / | (_) | | | | || (_) || |_  |  __/| (_| || || |_ 
      //  |___/   \\___/  |_| |_| \\___/  \\__|  \\___| \\__,_||_| \\__|
      //
      // Generated by skir-go-gen
      // Home: https://github.com/gepheum/skir-go-gen
      //
      // To install the Skir client library, run:
      //   go get github.com/gepheum/skir-go-client

      package `,
      packageName,
      "_skir\n\n",
    );

    this.writeImports();

    for (const record of this.inModule.records) {
      const { recordType } = record.record;
      if (recordType === "struct") {
        this.writeTypesForStruct(record);
      } else {
        this.writeTypesForEnum(record);
      }
    }

    if (this.inModule.methods.length) {
      this.pushSeparator("Methods");
      for (const method of this.inModule.methods) {
        this.writeMethod(method);
      }
    }

    if (this.inModule.constants.length) {
      this.pushSeparator("Constants");
      for (const constant of this.inModule.constants) {
        this.writeConstant(constant);
      }
    }

    // To disable unused import errors.
    this.push(
      "var _ = skir_client.Array[bool]{}\n",
      "var _ = atomic.Pointer[bool]{}\n",
      "var _ = time.Time{}\n",
    );

    return this.joinLinesAndFixFormatting();
  }

  private writeImports(): void {
    // TODO: rm e2e-test
    this.push(`import (
      "e2e-test/skir_client"
      "sync/atomic"
      "time"
    `);
    for (const importedPath of Object.keys(this.inModule.pathToImportedNames)) {
      const alias = modulePathToAlias(importedPath);
      const packagePath = modulePathToPackagePath(importedPath);
      this.push(
        `${alias} "${this.config.goModuleName}/skirout/${packagePath}"\n`,
      );
    }
    this.push(")\n\n");
  }

  private writeTypesForStruct(struct: RecordLocation): void {
    this.pushSeparator(
      "struct ".concat(
        struct.recordAncestors.map((r) => r.name.text).join("."),
      ),
    );
    const { typeSpeller } = this;
    const fields = [...struct.record.fields].sort((a, b) =>
      a.name.text.localeCompare(b.name.text),
    );
    const className = getClassName(struct);

    // Helper: for a field, determine getterName and return type in the interface.
    // Struct fields: return the interface type (not a pointer).
    // Optional fields: return skir_client.Optional[T].
    const getInterfaceReturnType = (field: (typeof fields)[0]): string => {
      const ft = field.type!;
      if (ft.kind === "optional") {
        return `skir_client.Optional[${typeSpeller.getGoType(ft.other)}]`;
      }
      return typeSpeller.getGoType(ft);
    };

    // Define the interface.
    this.push(
      commentify([docToCommentText(struct.record.doc), "Deeply immutable."]),
    );
    this.push(`type ${className} interface {\n`);
    for (const field of fields) {
      const getterName = structFieldToGetterName(field);
      this.push(`${getterName}() ${getInterfaceReturnType(field)}\n`);
    }
    // Search methods for keyed array fields belong in the interface too.
    for (const field of fields) {
      const keyedArrayHelper = this.getKeyedArrayHelper(field!);
      if (!keyedArrayHelper) continue;
      const { searchMethodName } = keyedArrayHelper;
      const { exposedKeyType, itemType } = keyedArrayHelper;
      this.push(
        `${searchMethodName}(k ${exposedKeyType}) skir_client.Optional[${itemType}]\n`,
      );
    }
    this.push(`ToBuilder() *${className}_partialBuilderType\n`);
    this.push(`String() string\n`);
    this.push(`unrecognized() *skir_client.Internal__UnrecognizedFields\n`);
    this.push(`isDefaultInstance() bool\n`);
    this.push("}\n\n");

    // Define the _impl struct.
    this.push(`type _${className}_impl struct {\n`);
    for (const field of fields) {
      const fieldName = "_" + convertCase(field.name.text, "lowerCamel");
      const goType = typeSpeller.getGoType(field.type!);
      this.push(`${fieldName} ${goType}\n`);
      const keyedArrayHelper = this.getKeyedArrayHelper(field);
      if (keyedArrayHelper) {
        const { mapType } = keyedArrayHelper;
        this.push(`${fieldName}_indexed *atomic.Pointer[${mapType}]\n`);
      }
    }
    this.push(`__unrecognized *skir_client.Internal__UnrecognizedFields\n`);
    this.push("}\n\n");

    // Write the getters on *_${className}_impl.
    for (const field of fields) {
      const getterName = structFieldToGetterName(field);
      const ft = field.type!;
      const fieldAccess = "s._" + convertCase(field.name.text, "lowerCamel");
      const retType = getInterfaceReturnType(field);
      this.push(`func (s *_${className}_impl) ${getterName}() ${retType} {\n`);
      if (this.isStructType(ft) && field.isRecursive === "hard") {
        // Interface field, nil = default
        const otherRecord = typeSpeller.recordMap.get(
          (ft as { kind: "record"; key: RecordKey }).key,
        )!;
        const otherClassName = getClassName(otherRecord);
        this.push(`if ${fieldAccess} == nil {\n`);
        this.push(`  return ${otherClassName}_default()\n`);
        this.push("}\n");
        this.push(`return ${fieldAccess}\n`);
      } else {
        this.push(`return ${fieldAccess}\n`);
      }
      this.push("}\n\n");
    }

    // Write unrecognized() on *_${className}_impl.
    this.push(
      `func (s *_${className}_impl) unrecognized() *skir_client.Internal__UnrecognizedFields {\n`,
    );
    this.push(`return s.__unrecognized\n`);
    this.push("}\n\n");

    // Write isDefaultInstance() on *_${className}_impl.
    this.push(`func (s *_${className}_impl) isDefaultInstance() bool {\n`);
    this.push(`return s == &_${className}_default_impl\n`);
    this.push("}\n\n");

    // Write the Search methods for keyed array fields.
    for (const field of fields) {
      const keyedArrayHelper = this.getKeyedArrayHelper(field);
      if (!keyedArrayHelper) continue;
      const {
        searchMethodName,
        mapType,
        itemType,
        exposedKeyType,
        itemToComparableExpr,
        exposedKeyToComparableExpr,
      } = keyedArrayHelper;
      const fieldName = convertCase(field.name.text, "UpperCamel");
      const rawFieldAccess = "s._" + convertCase(field.name.text, "lowerCamel");
      const indexingAccess = `${rawFieldAccess}_indexed`;
      this.push(
        commentify([
          `${searchMethodName} returns the last element in ${fieldName} whose key equals k.`,
          "Returns an absent Optional if no such element exists.",
        ]),
      );
      this.push(
        `func (s *_${className}_impl) ${searchMethodName}(k ${exposedKeyType}) skir_client.Optional[${itemType}] {\n`,
      );
      this.push(`indexingPtr := ${indexingAccess}\n`);
      this.push("m := indexingPtr.Load()\n");
      this.push("if m == nil {\n");
      this.push(`newMap := make(${mapType}, ${rawFieldAccess}.Len())\n`);
      this.push(`for _, e := range ${rawFieldAccess}.All() {\n`);
      this.push(`newMap[${itemToComparableExpr}] = e\n`);
      this.push("}\n");
      this.push("indexingPtr.Store(&newMap)\n");
      this.push("m = &newMap\n");
      this.push("}\n");
      this.push(`v, ok := (*m)[${exposedKeyToComparableExpr}]\n`);
      this.push("if !ok {\n");
      this.push(`return skir_client.Absent[${itemType}]()\n`);
      this.push("}\n");
      this.push(`return skir_client.OptionalOf(v)\n`);
      this.push("}\n\n");
    }

    // Write the String() method
    this.push(`func (s *_${className}_impl) String() string {\n`);
    this.push(
      `return ${className}_serializer().ToJson(s, skir_client.Readable{})\n`,
    );
    this.push("}\n\n");

    // Write ToBuilder() method (on value receiver so both value and pointer implement it).
    this.push(
      commentify([
        `ToBuilder returns a new builder with all fields copied from this ${className}.`,
        "Set the fields you want to change, then call Build() to get a modified copy.",
      ]),
    );
    this.push(
      `func (s *_${className}_impl) ToBuilder() *${className}_partialBuilderType {\n`,
    );
    this.push(`  return &${className}_partialBuilderType{s: *s}\n`);
    this.push("}\n\n");

    // Define the builder interfaces.
    const fieldToBuilderName = (f: Field | undefined): string =>
      f
        ? className + "_builderAt" + convertCase(f.name.text, "UpperCamel")
        : className + "_builderDone";
    for (const [i, field] of fields.entries()) {
      const fieldName = convertCase(field.name.text, "UpperCamel");
      const fieldType = field.type!;
      const nextField = fields[i + 1];
      const paramType = getInterfaceReturnType(field);
      this.push(`type ${fieldToBuilderName(field)} interface {\n`);
      const returnType = fieldToBuilderName(nextField);
      this.push(commentify(docToCommentText(field.doc)));
      this.push(`Set${fieldName}(v ${paramType}) ${returnType}\n`);
      if (fieldType.kind === "array") {
        const itemType = typeSpeller.getGoType(fieldType.item);
        this.push(`Set${fieldName}_FromSlice(v []${itemType}) ${returnType}\n`);
      }
      this.push("}\n\n");
    }
    this.push(`type ${className}_builderDone interface {\n`);
    this.push(`  Build() ${className}\n`);
    this.push("}\n\n");

    // Define the builder struct (stores _impl internally).
    this.push(`type _${className}_builder struct {\n`);
    this.push(`s _${className}_impl\n`);
    this.push("}\n\n");

    // Write the setters of the ordered builder.
    for (const [i, field] of fields.entries()) {
      const builderName = `_${className}_builder`;
      const fieldName = convertCase(field.name.text, "UpperCamel");
      const fieldType = field.type!;
      const paramType = getInterfaceReturnType(field);
      const nextField = fields[i + 1];
      const returnType = fieldToBuilderName(nextField);
      this.push(
        `func (b *${builderName}) Set${fieldName}(v ${paramType}) ${returnType} {\n`,
      );
      const fieldAccess = "b.s._" + convertCase(field.name.text, "lowerCamel");
      this.writeBuilderSetterBody(field, fieldAccess, "v");
      this.push("  return b\n");
      this.push("}\n\n");
      if (fieldType.kind === "array") {
        const itemType = typeSpeller.getGoType(fieldType.item);
        this.push(
          `func (b *${builderName}) Set${fieldName}_FromSlice(v []${itemType}) ${returnType} {\n`,
        );
        this.push(
          `  return b.Set${fieldName}(skir_client.ArrayFromSlice(v))\n`,
        );
        this.push("}\n\n");
      }
    }

    // Write the Build() method of the ordered builder.
    this.push(`func (b *_${className}_builder) Build() ${className} {\n`);
    this.push("  copy := b.s\n");
    this.push("  return &copy\n");
    this.push("}\n\n");

    // Write the builder factory function.
    this.push(
      commentify([
        `${className}_builder returns a new builder for constructing ${className} values.`,
        "All fields must be set in alphabetical order before calling Build().",
      ]),
    );
    this.push(
      `func ${className}_builder() ${fieldToBuilderName(fields[0])} {\n`,
    );
    this.push(`  return &_${className}_builder{}\n`);
    this.push("}\n\n");

    // Define the partial builder type (stores _impl internally).
    this.push(`type ${className}_partialBuilderType struct {\n`);
    this.push(`s _${className}_impl\n`);
    this.push("}\n\n");

    // Write the setters for the partial builder.
    for (const field of fields) {
      const builderTypeName = `${className}_partialBuilderType`;
      const fieldName = convertCase(field.name.text, "UpperCamel");
      const fieldType = field.type!;
      const paramType = getInterfaceReturnType(field);
      this.push(commentify(docToCommentText(field.doc)));
      this.push(
        `func (b *${builderTypeName}) Set${fieldName}(v ${paramType}) *${builderTypeName} {\n`,
      );
      const fieldAccess = "b.s._" + convertCase(field.name.text, "lowerCamel");
      this.writeBuilderSetterBody(field, fieldAccess, "v");
      this.push("  return b\n");
      this.push("}\n\n");
      if (fieldType.kind === "array") {
        const itemType = typeSpeller.getGoType(fieldType.item);
        this.push(
          `func (b *${builderTypeName}) Set${fieldName}_FromSlice(v []${itemType}) *${builderTypeName} {\n`,
        );
        this.push(
          `  return b.Set${fieldName}(skir_client.ArrayFromSlice(v))\n`,
        );
        this.push("}\n\n");
      }
    }

    // Write Build() for the partial builder.
    this.push(
      `func (b *${className}_partialBuilderType) Build() ${className} {\n`,
    );
    this.push("  return &b.s\n");
    this.push("}\n\n");

    // Write the partial builder factory function.
    this.push(
      commentify([
        `${className}_partialBuilder returns a new builder for constructing ${className} values.`,
        "All fields pre-initialized to their default values. Fields can be set in any order.",
      ]),
    );
    this.push(
      `func ${className}_partialBuilder() *${className}_partialBuilderType {\n`,
    );
    this.push(
      `  return &${className}_partialBuilderType{s: _${className}_default_impl}\n`,
    );
    this.push("}\n\n");

    // Default _impl value.
    this.push(`var _${className}_default_impl = _${className}_impl{\n`);
    for (const field of fields) {
      const fieldType = field.type!;
      const keyedArrayHelper = this.getKeyedArrayHelper(field);
      if (keyedArrayHelper) {
        const { mapType } = keyedArrayHelper;
        const fieldName = "_"
          .concat(convertCase(field.name.text, "lowerCamel"))
          .concat("_indexed");
        this.push(`  ${fieldName}: &atomic.Pointer[${mapType}]{},\n`);
      } else if (this.isStructType(fieldType) && field.isRecursive !== "hard") {
        const fieldName = "_".concat(
          convertCase(field.name.text, "lowerCamel"),
        );
        const goType = typeSpeller.getGoType(fieldType);
        this.push(`  ${fieldName}: ${goType}_default(),\n`);
      }
    }
    this.push("}\n\n");

    // Default interface variable (points to _default_impl).
    this.push(
      `var _${className}_default ${className} = &_${className}_default_impl\n\n`,
    );

    this.push(
      commentify([
        `${className}_default returns the default ${className}.`,
        "All fields are set to their default values.",
      ]),
    );
    this.push(`func ${className}_default() ${className} {\n`);
    this.push(`  return _${className}_default\n`);
    this.push("}\n\n");

    // Adapter (typed on the interface).
    const qualifiedRecordName = struct.recordAncestors
      .map((r) => r.name.text)
      .join(".");
    this.push(
      `var _${className}_adapter = skir_client.Internal__NewStructAdapter(\n`,
    );
    this.push(`${className}_default(),\n`);
    this.push(`func(s ${className}) bool { return s.isDefaultInstance() },\n`);
    this.push(`${className}_partialBuilder,\n`);
    this.push(
      `func(b *${className}_partialBuilderType) ${className} { return b.Build() },\n`,
    );
    this.push(`${toGoStringLiteral(struct.modulePath)},\n`);
    this.push(`${toGoStringLiteral(qualifiedRecordName)},\n`);
    this.push(`${toGoStringLiteral(docToCommentText(struct.record.doc))},\n`);
    this.push(
      `func(s ${className}) *skir_client.Internal__UnrecognizedFields { return s.unrecognized() },\n`,
    );
    this.push(
      `func(b *${className}_partialBuilderType, u *skir_client.Internal__UnrecognizedFields) { b.s.__unrecognized = u },\n`,
    );
    this.push(")\n\n");

    // Serializer function.
    this.push(
      commentify(
        `${className}_serializer returns the serializer for ${className} values.`,
      ),
    );
    this.push(
      `func ${className}_serializer() skir_client.Serializer[${className}] {\n`,
    );
    this.push(`return _${className}_adapter.Serializer()\n`);
    this.push("}\n\n");

    // init() – initialize struct-type field defaults, add fields, finalize.
    this.push("func init() {\n");
    // Register fields with the adapter.
    for (const field of struct.record.fields) {
      const fieldName = "_" + convertCase(field.name.text, "lowerCamel");
      const getterName = structFieldToGetterName(field);
      const type = field.type!;
      const serializerExpr = typeSpeller.getSerializerExpression(type);
      const goType = typeSpeller.getGoType(type);
      const builderType = className.concat("_partialBuilderType");
      this.push(
        `skir_client.Internal__AddField(\n` +
          `_${className}_adapter,\n` +
          `${toGoStringLiteral(field.name.text)},\n` +
          `${field.number},\n` +
          `${serializerExpr},\n` +
          `${toGoStringLiteral(docToCommentText(field.doc))},\n` +
          `func(s ${className}) ${goType} { return s.${getterName}() },\n` +
          `func(b *${builderType}, v ${goType}) { b.s.${fieldName} = v },\n` +
          ")\n",
      );
    }
    for (const removedNumber of struct.record.removedNumbers) {
      this.push(`_${className}_adapter.AddRemovedNumber(${removedNumber})\n`);
    }
    this.push(`_${className}_adapter.Finalize()\n`);
    this.push("}\n\n");
  }

  // Writes the body of a builder setter for one field.
  // fieldAccess: Go expression for the field (e.g. "b.s._foo")
  // varName: the parameter variable name, e.g. "v"
  private writeBuilderSetterBody(
    field: Field,
    fieldAccess: string,
    varName: string,
  ): void {
    const type = field.type!;
    if (type.kind === "array") {
      // Array: direct assignment + reset indexed cache if keyed
      this.push(`  ${fieldAccess} = ${varName}\n`);
      const keyedArrayHelper = this.getKeyedArrayHelper(field);
      if (keyedArrayHelper) {
        this.push(
          `  ${fieldAccess}_indexed = &atomic.Pointer[${keyedArrayHelper.mapType}]{}\n`,
        );
      }
    } else {
      this.push(`  ${fieldAccess} = ${varName}\n`);
    }
  }

  private writeTypesForEnum(record: RecordLocation): void {
    this.pushSeparator(
      "enum ".concat(record.recordAncestors.map((r) => r.name.text).join(".")),
    );
    const { typeSpeller } = this;
    const variants = [...record.record.fields].sort((a, b) =>
      a.name.text.localeCompare(b.name.text),
    );
    const className = getClassName(record);
    const constantVariants = variants.filter((v) => !v.type);
    const wrapperVariants = variants.filter((v) => v.type);
    const kindType = `${className}_kind`;

    // Define the Kind type and its constants.
    this.push(
      `// ${kindType} identifies which variant a ${className} holds.\n`,
    );
    this.push(`type ${kindType} int\n\n`);
    this.push(`const (\n`);
    this.push(`${kindType}_unknown ${kindType} = iota\n`);
    for (const variant of constantVariants) {
      const lowerName = convertCase(variant.name.text, "lowerCamel");
      this.push(`${kindType}_${lowerName}Const\n`);
    }
    for (const variant of wrapperVariants) {
      const lowerName = convertCase(variant.name.text, "lowerCamel");
      this.push(`${kindType}_${lowerName}Wrapper\n`);
    }
    this.push(")\n\n");

    // Define the value interface and its implementations.
    const valueInterfaceName = `_${className}_value`;
    this.push(`type ${valueInterfaceName} interface {\n`);
    this.push(`GetUnrecognized() *skir_client.Internal__UnrecognizedVariant\n`);
    for (const variant of wrapperVariants) {
      const upperName = convertCase(variant.name.text, "UpperCamel");
      const goType = typeSpeller.getGoType(variant.type!);
      this.push(`Unwrap${upperName}() *${goType}\n`);
    }
    this.push("}\n\n");
    // Unknown implementation.
    const unknownImplName = `_${className}_value_unknown`;
    this.push(`type ${unknownImplName} struct {\n`);
    this.push(`v skir_client.Internal__UnrecognizedVariant\n`);
    this.push("}\n");
    this.push(
      `func (w *${unknownImplName}) GetUnrecognized() *skir_client.Internal__UnrecognizedVariant {\n`,
    );
    this.push("return &w.v\n");
    this.push("}\n");
    for (const variant of wrapperVariants) {
      const upperName = convertCase(variant.name.text, "UpperCamel");
      const goType = typeSpeller.getGoType(variant.type!);
      this.push(
        `func (w *${unknownImplName}) Unwrap${upperName}() *${goType} {\n`,
      );
      this.push("return nil\n");
      this.push("}\n");
    }
    this.push("\n");
    // Wrapper variant implementations.
    for (const variant of wrapperVariants) {
      const lowerName = convertCase(variant.name.text, "lowerCamel");
      const implName = `_${className}_value_${lowerName}Wrapper`;
      const goType = typeSpeller.getGoType(variant.type!);
      this.push(`type ${implName} struct {\n`);
      this.push(`v ${goType}\n`);
      this.push("}\n");
      this.push(
        `func (w *${implName}) GetUnrecognized() *skir_client.Internal__UnrecognizedVariant {\n`,
      );
      this.push("return nil\n");
      this.push("}\n");
      for (const other of wrapperVariants) {
        const otherUpperName = convertCase(other.name.text, "UpperCamel");
        const otherGoType = typeSpeller.getGoType(other.type!);
        this.push(
          `func (w *${implName}) Unwrap${otherUpperName}() *${otherGoType} {\n`,
        );
        if (other.name.text === variant.name.text) {
          this.push("return &w.v\n");
        } else {
          this.push("return nil\n");
        }
        this.push("}\n");
      }
      this.push("\n");
    }

    // Define the frozen enum struct.
    this.push(
      commentify([docToCommentText(record.record.doc), "Deeply immutable."]),
    );
    this.push(`type ${className} struct {\n`);
    this.push(`kind ${kindType}\n`);
    this.push(`value ${valueInterfaceName}\n`);
    this.push("}\n\n");

    // Kind() getter.
    this.push(
      commentify(`Kind returns which variant this ${className} holds.`),
    );
    this.push(`func (e ${className}) Kind() ${kindType} {\n`);
    this.push("return e.kind\n");
    this.push("}\n\n");

    // IsUnknown() method.
    this.push(
      commentify(
        `IsUnknown returns true if this ${className} holds the unknown variant.`,
      ),
    );
    this.push(`func (e ${className}) IsUnknown() bool {\n`);
    this.push(`return e.kind == ${kindType}_unknown\n`);
    this.push("}\n\n");

    // Is...() methods for constant variants.
    for (const variant of constantVariants) {
      const variantName = variant.name.text;
      const lowerName = convertCase(variantName, "lowerCamel");
      const upperName = convertCase(variantName, "UpperCamel");
      this.push(
        commentify([
          `Is${upperName}Const returns true if this ${className} holds the '${variantName}' constant variant.`,
          docToCommentText(variant.doc),
        ]),
      );
      this.push(`func (e ${className}) Is${upperName}Const() bool {\n`);
      this.push(`return e.kind == ${kindType}_${lowerName}Const\n`);
      this.push("}\n\n");
    }

    // Is...() methods for wrapper variants.
    for (const variant of wrapperVariants) {
      const variantName = variant.name.text;
      const lowerName = convertCase(variantName, "lowerCamel");
      const upperName = convertCase(variantName, "UpperCamel");
      this.push(
        commentify([
          `Is${upperName}Wrapper returns true if this ${className} holds the '${variantName}' wrapper variant.`,
          docToCommentText(variant.doc),
        ]),
      );
      this.push(`func (e ${className}) Is${upperName}Wrapper() bool {\n`);
      this.push(`return e.kind == ${kindType}_${lowerName}Wrapper\n`);
      this.push("}\n\n");
    }

    // Factory functions for constant variants.
    this.push(
      commentify([
        `${className}_unknown creates an instance holding the unknown variant.`,
        `It is the default value for fields of type ${className}.`,
      ]),
    );
    this.push(`func ${className}_unknown() ${className} {\n`);
    this.push(`return ${className}{}\n`);
    this.push("}\n\n");
    for (const variant of constantVariants) {
      const lowerName = convertCase(variant.name.text, "lowerCamel");
      this.push(commentify(docToCommentText(variant.doc)));
      this.push(`func ${className}_${lowerName}Const() ${className} {\n`);
      this.push(`return ${className}{kind: ${kindType}_${lowerName}Const}\n`);
      this.push("}\n\n");
    }

    // Factory functions for wrapper variants.
    for (const variant of wrapperVariants) {
      const variantName = variant.name.text;
      const lowerName = convertCase(variantName, "lowerCamel");
      const variantType = variant.type!;
      const goType = typeSpeller.getGoType(variantType);
      this.push(
        commentify([
          `${className}_${lowerName}Wrapper creates a '${variantName}' variant wrapping the given value.`,
          docToCommentText(variant.doc),
        ]),
      );
      const implName = `_${className}_value_${lowerName}Wrapper`;
      this.push(
        `func ${className}_${lowerName}Wrapper(v ${goType}) ${className} {\n`,
      );
      this.push(
        `return ${className}{kind: ${kindType}_${lowerName}Wrapper, value: &${implName}{v: v}}\n`,
      );
      this.push("}\n\n");
    }

    // Unwrap...() methods for wrapper variants.
    for (const variant of wrapperVariants) {
      const variantName = variant.name.text;
      const lowerName = convertCase(variantName, "lowerCamel");
      const upperName = convertCase(variantName, "UpperCamel");
      const type = variant.type!;
      const goType = typeSpeller.getGoType(type);
      this.push(
        commentify([
          `Unwrap${upperName} returns the '${variantName}' value wrapped in this ${className}.`,
          `Assumes that Is${upperName}() is true. Panics otherwise.`,
        ]),
      );
      this.push(`func (e ${className}) Unwrap${upperName}() ${goType} {\n`);
      this.push(`if e.kind != ${kindType}_${lowerName}Wrapper {\n`);
      this.push(
        `panic("${className}.Unwrap${upperName}(): kind is not ${upperName}Wrapper")\n`,
      );
      this.push("}\n");
      this.push(`return *e.value.Unwrap${upperName}()\n`);
      this.push("}\n\n");
    }

    // Visitor interface.
    // Note: Go does not allow type parameters on methods, so Accept is a
    // package-level generic function rather than a method on the enum.
    this.push(
      commentify([
        `${className}_visitor is implemented by types that handle all variants of ${className}.`,
        "Pass an implementation to ${className}_accept.",
      ]),
    );
    this.push(`type ${className}_visitor[T any] interface {\n`);
    this.push("OnUnknown() T\n");
    for (const variant of constantVariants) {
      const upperName = convertCase(variant.name.text, "UpperCamel");
      this.push(`On${upperName}Const() T\n`);
    }
    for (const variant of wrapperVariants) {
      const upperName = convertCase(variant.name.text, "UpperCamel");
      const goType = typeSpeller.getGoType(variant.type!);
      this.push(`On${upperName}Wrapper(v ${goType}) T\n`);
    }
    this.push("}\n\n");

    // Accept function.
    this.push(
      commentify(
        `${className}_accept calls the appropriate method on v corresponding to the variant held by e.`,
      ),
    );
    this.push(
      `func ${className}_accept[T any](e ${className}, v ${className}_visitor[T]) T {\n`,
    );
    this.push("switch e.kind {\n");
    for (const variant of constantVariants) {
      const variantName = variant.name.text;
      const lowerName = convertCase(variantName, "lowerCamel");
      const upperName = convertCase(variantName, "UpperCamel");
      this.push(`case ${kindType}_${lowerName}Const:\n`);
      this.push(`return v.On${upperName}Const()\n`);
    }
    for (const variant of wrapperVariants) {
      const variantName = variant.name.text;
      const lowerName = convertCase(variantName, "lowerCamel");
      const upperName = convertCase(variantName, "UpperCamel");
      this.push(`case ${kindType}_${lowerName}Wrapper:\n`);
      this.push(`return v.On${upperName}Wrapper(e.Unwrap${upperName}())\n`);
    }
    this.push("default:\n");
    this.push("return v.OnUnknown()\n");
    this.push("}\n");
    this.push("}\n\n");

    // String() method.
    this.push(`func (e ${className}) String() string {\n`);
    this.push(
      `return ${className}_serializer().ToJson(e, skir_client.Readable{})\n`,
    );
    this.push("}\n\n");

    // Adapter.
    const qualifiedRecordName = record.recordAncestors
      .map((r) => r.name.text)
      .join(".");
    const kindCount = 1 + constantVariants.length + wrapperVariants.length;
    this.push(`var _${className}_adapter = skir_client.NewEnumAdapter(\n`);
    this.push(`${toGoStringLiteral(record.modulePath)},\n`);
    this.push(`${toGoStringLiteral(qualifiedRecordName)},\n`);
    this.push(`${toGoStringLiteral(docToCommentText(record.record.doc))},\n`);
    this.push(`func(e ${className}) int { return int(e.kind) },\n`);
    this.push(`${kindCount},\n`);
    this.push(`${className}{},\n`);
    this.push(
      `func(u *skir_client.Internal__UnrecognizedVariant) ${className} {\n` +
        `if u == nil { return ${className}{} }\n` +
        `return ${className}{value: &_${className}_value_unknown{v: *u}}\n` +
        `},\n`,
    );
    this.push(
      `func(e ${className}) *skir_client.Internal__UnrecognizedVariant {\n` +
        `if e.kind != ${kindType}_unknown {\n` +
        `return nil\n` +
        `}\n` +
        `if e.value == nil {\n` +
        `return nil\n` +
        `}\n` +
        `return e.value.GetUnrecognized()\n` +
        `},\n`,
    );
    this.push(")\n\n");

    // Serializer function.
    this.push(
      commentify(
        `${className}_serializer returns the serializer for ${className} values.`,
      ),
    );
    this.push(
      `func ${className}_serializer() skir_client.Serializer[${className}] {\n`,
    );
    this.push(`return _${className}_adapter.Serializer()\n`);
    this.push("}\n\n");

    // init() – register variants and finalize.
    this.push("func init() {\n");
    for (const variant of constantVariants) {
      const lowerName = convertCase(variant.name.text, "lowerCamel");
      this.push(
        `_${className}_adapter.AddConstantVariant(\n` +
          `${variant.number},\n` +
          `${toGoStringLiteral(variant.name.text)},\n` +
          `int(${kindType}_${lowerName}Const),\n` +
          `${toGoStringLiteral(docToCommentText(variant.doc))},\n` +
          `${className}_${lowerName}Const(),\n` +
          ")\n",
      );
    }
    for (const variant of wrapperVariants) {
      const variantName = variant.name.text;
      const lowerName = convertCase(variantName, "lowerCamel");
      const upperName = convertCase(variantName, "UpperCamel");
      const goType = typeSpeller.getGoType(variant.type!);
      const serializerExpr = typeSpeller.getSerializerExpression(variant.type!);
      this.push(
        `skir_client.Internal__AddWrapperVariant(\n` +
          `_${className}_adapter,\n` +
          `${variant.number},\n` +
          `${toGoStringLiteral(variant.name.text)},\n` +
          `int(${kindType}_${lowerName}Wrapper),\n` +
          `${serializerExpr},\n` +
          `${toGoStringLiteral(docToCommentText(variant.doc))},\n` +
          `func(v ${goType}) ${className} { return ${className}_${lowerName}Wrapper(v) },\n` +
          `func(e ${className}) ${goType} { return e.Unwrap${upperName}() },\n` +
          ")\n",
      );
    }
    for (const removedNumber of record.record.removedNumbers) {
      this.push(`_${className}_adapter.AddRemovedNumber(${removedNumber})\n`);
    }
    this.push(`_${className}_adapter.Finalize()\n`);
    this.push("}\n\n");
  }

  private writeMethod(method: Method): void {
    const { typeSpeller } = this;
    const name = method.name.text;
    const requestGoType = typeSpeller.getGoType(method.requestType!);
    const responseGoType = typeSpeller.getGoType(method.responseType!);
    const requestSerializerExpr = typeSpeller.getSerializerExpression(
      method.requestType!,
    );
    const responseSerializerExpr = typeSpeller.getSerializerExpression(
      method.responseType!,
    );
    this.push(commentify(docToCommentText(method.doc)));
    this.push(
      `func ${convertCase(name, "UpperCamel")}() skir_client.Method[${requestGoType}, ${responseGoType}]{\n`,
    );
    this.push("return skir_client.NewMethod(\n");
    this.push(`${toGoStringLiteral(name)},\n`);
    this.push(`${method.number},\n`);
    this.push(`${requestSerializerExpr},\n`);
    this.push(`${responseSerializerExpr},\n`);
    this.push(`${toGoStringLiteral(docToCommentText(method.doc))},\n`);
    this.push(")\n");
    this.push("}\n\n");
  }

  private writeConstant(constant: Constant): void {
    const { typeSpeller } = this;
    const goName = convertCase(constant.name.text, "UpperCamel").concat(
      "_const",
    );
    const type = constant.type!;
    const goType = typeSpeller.getGoType(type);
    this.push(commentify(docToCommentText(constant.doc)));
    const goLiteral = tryGetGoLiteral(constant);
    if (goLiteral) {
      this.push(`const ${goName} ${goType} = ${goLiteral}\n\n`);
    } else {
      const serializerExpr = typeSpeller.getSerializerExpression(type);
      const goStringLiteral = toGoStringLiteral(
        JSON.stringify(constant.valueAsDenseJson),
      );
      this.push(`var _${goName} ${goType}\n\n`);
      this.push("func init() {\n");
      this.push(`  v, _ := ${serializerExpr}.FromJson(${goStringLiteral})\n`);
      this.push(`  _${goName} = v\n`);
      this.push("}\n\n");
      this.push(`func ${goName}() ${goType} {\n`);
      this.push(`  return _${goName}\n`);
      this.push("}\n\n");
    }
  }

  private getKeyedArrayHelper(field: Field): KeyedArrayHelper | null {
    const type = field.type!;
    if (type.kind !== "array") {
      return null;
    }
    const { key } = type;
    if (!key) {
      return null;
    }
    const searchMethodName = convertCase(field.name.text, "UpperCamel")
      .concat("_SearchBy")
      .concat(
        key.path.map((p) => convertCase(p.name.text, "UpperCamel")).join(""),
      );
    const keyAccessor = "e.".concat(
      key.path
        .map((p) => structFieldToGetterName(p.name.text).concat("()"))
        .join("."),
    );
    const { typeSpeller } = this;
    const itemType = typeSpeller.getGoType(type.item);
    const makeMapType = (comp: string): string => `map[${comp}]${itemType}`;
    const { keyType } = key;
    switch (keyType.kind) {
      case "primitive": {
        switch (keyType.primitive) {
          case "bool":
          case "int32":
          case "int64":
          case "float32":
          case "float64":
          case "string":
          case "hash64": {
            // The simple case: the key type is already comparable.
            const comparableType = typeSpeller.getGoType(keyType);
            return {
              searchMethodName: searchMethodName,
              itemType: itemType,
              mapType: makeMapType(comparableType),
              exposedKeyType: comparableType,
              itemToComparableExpr: keyAccessor,
              exposedKeyToComparableExpr: "k",
            };
          }
          case "timestamp":
            return {
              searchMethodName: searchMethodName,
              itemType: itemType,
              mapType: makeMapType("int64"),
              exposedKeyType: "time.Time",
              itemToComparableExpr: keyAccessor.concat(".UnixMilli()"),
              exposedKeyToComparableExpr: "k.UnixMilli()",
            };
          case "bytes":
            return {
              searchMethodName: searchMethodName,
              itemType: itemType,
              mapType: makeMapType("string"),
              exposedKeyType: "skir_client.Bytes",
              itemToComparableExpr: keyAccessor.concat(".Hex()"),
              exposedKeyToComparableExpr: "k.Hex()",
            };
        }
        break;
      }
      case "record": {
        const comparableType = typeSpeller.getGoType(keyType).concat("_kind");
        return {
          searchMethodName: searchMethodName,
          itemType: itemType,
          mapType: makeMapType(comparableType),
          exposedKeyType: comparableType,
          itemToComparableExpr: keyAccessor,
          exposedKeyToComparableExpr: "k",
        };
      }
    }
  }

  private isStructType(type: ResolvedType): boolean {
    return (
      type.kind === "record" &&
      this.typeSpeller.recordMap.get(type.key)?.record.recordType === "struct"
    );
  }

  private pushSeparator(header: string): void {
    this.push(`// ${"=".repeat(78)}\n`);
    this.push(`// ${header}\n`);
    this.push(`// ${"=".repeat(78)}\n\n`);
  }

  private push(...code: string[]): void {
    this.code += code.join("");
  }

  private joinLinesAndFixFormatting(): string {
    const indentUnit = "\t";
    let result = "";
    // The indent at every line is obtained by repeating indentUnit N times,
    // where N is the length of this array.
    const contextStack: Array<"{" | "(" | "[" | "<" | ":" | "."> = [];
    // Returns the last element in `contextStack`.
    const peakTop = (): string => contextStack.at(-1)!;
    const getMatchingLeftBracket = (r: "}" | ")" | "]" | ">"): string => {
      switch (r) {
        case "}":
          return "{";
        case ")":
          return "(";
        case "]":
          return "[";
        case ">":
          return "<";
      }
    };
    for (let line of this.code.split("\n")) {
      line = line.trim();
      if (line.length <= 0) {
        // Don't indent empty lines.
        result += "\n";
        continue;
      }

      const firstChar = line[0];
      switch (firstChar) {
        case "}":
        case ")":
        case "]":
        case ">": {
          const left = getMatchingLeftBracket(firstChar);
          while (contextStack.pop() !== left) {
            if (contextStack.length <= 0) {
              throw Error();
            }
          }
          break;
        }
        case ".": {
          if (peakTop() !== ".") {
            contextStack.push(".");
          }
          break;
        }
      }
      const indent =
        indentUnit.repeat(contextStack.length) +
        (line.startsWith("*") ? " " : "");
      result += `${indent}${line.trimEnd()}\n`;
      if (line.startsWith("/") || line.startsWith("*")) {
        // A comment.
        continue;
      }
      const lastChar = line.slice(-1);
      switch (lastChar) {
        case "{":
        case "(":
        case "[":
        case "<": {
          // The next line will be indented
          contextStack.push(lastChar);
          break;
        }
        case ":":
        case "=": {
          if (peakTop() !== ":") {
            contextStack.push(":");
          }
          break;
        }
        case ";":
        case ",": {
          if (peakTop() === "." || peakTop() === ":") {
            contextStack.pop();
          }
        }
      }
    }

    return (
      result
        // Remove spaces enclosed within curly brackets if that's all there is.
        .replace(/\{\s+\}/g, "{}")
        // Remove spaces enclosed within round brackets if that's all there is.
        .replace(/\(\s+\)/g, "()")
        // Remove spaces enclosed within square brackets if that's all there is.
        .replace(/\[\s+\]/g, "[]")
        // Remove empty line following an open curly bracket.
        .replace(/(\{\n *)\n/g, "$1")
        // Remove empty line preceding a closed curly bracket.
        .replace(/\n(\n *\})/g, "$1")
        // Coalesce consecutive empty lines.
        .replace(/\n\n\n+/g, "\n\n")
        .replace(/\n\n$/g, "\n")
    );
  }

  private readonly typeSpeller: TypeSpeller;
  private code = "";
}

interface KeyedArrayHelper {
  /** Name of the generated Search method. */
  readonly searchMethodName: string;
  /** Item (Go) type. */
  readonly itemType: string;
  /** Go type of the internal map. */
  readonly mapType: string;
  /** Key (Go) type exposed to the user. */
  readonly exposedKeyType: string;
  /** Go expression for extracting a comparable key from an item (e). */
  readonly itemToComparableExpr: string;
  /** Go expression for extracting a comparable key from an exposed key (k). */
  readonly exposedKeyToComparableExpr: string;
}

function toGoStringLiteral(input: string): string {
  const escaped = input
    .replace(/\\/g, "\\\\") // Escape backslashes
    .replace(/"/g, '\\"') // Escape double quotes
    .replace(/\n/g, "\\n") // Escape newlines
    .replace(/\r/g, "\\r") // Escape carriage returns
    .replace(/\t/g, "\\t"); // Escape tabs
  return `"${escaped}"`;
}

function tryGetGoLiteral(constant: Constant): string | null {
  const type = constant.type!;
  if (type.kind !== "primitive") {
    return null;
  }
  const valueAsDenseJson = constant.valueAsDenseJson!;
  switch (type.primitive) {
    case "bool":
      return valueAsDenseJson ? "true" : "false";
    case "int32":
    case "int64":
    case "hash64":
    case "float32":
    case "float64": {
      const maybeQuoted = valueAsDenseJson.toString();
      if (
        maybeQuoted === "Infinity" ||
        maybeQuoted === "-Infinity" ||
        maybeQuoted === "NaN"
      ) {
        return null;
      } else {
        return maybeQuoted.startsWith('"') && maybeQuoted.endsWith('"')
          ? maybeQuoted.slice(1, -1)
          : maybeQuoted;
      }
    }
    case "string":
      return toGoStringLiteral(valueAsDenseJson as string);
  }
  return null;
}

function commentify(textOrLines: string | readonly string[]): string {
  const text = (
    typeof textOrLines === "string" ? textOrLines : textOrLines.join("\n")
  )
    .trim()
    .replace(/\n{3,}/g, "\n\n");
  if (text.length <= 0) {
    return "";
  }
  return text
    .split("\n")
    .map((line) => (line.length > 0 ? `// ${line}\n` : `//\n`))
    .join("");
}

function docToCommentText(doc: Doc): string {
  return doc.pieces
    .map((p) => {
      switch (p.kind) {
        case "text":
          return p.text;
        case "reference":
          return "`" + p.referenceRange.text.slice(1, -1) + "`";
      }
    })
    .join("");
}

